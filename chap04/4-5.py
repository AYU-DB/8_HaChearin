# \n
print("백문이 불여일견\n백견이 불여일타")

# \" \'
print("저는 '3학년'입니다.")
print('저는 "3학년"입니다.')
print("저는 \"3학년\"입니다.")
print("저는 \'3학년\'입니다.")

