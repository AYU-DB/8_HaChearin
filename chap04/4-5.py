print("/\\/\\ << 눈 웃음")

# \r : 커서를 맨 앞으로
# Pine 이 맨 앞자리에 출력이 됨
# 결과 : Pine sky
print("Blue sky\rDark")

# \b : 백스페이스(한 글자 삭제)
print("Bluee\b sky")

# \t : 탭
print("Blue\tsky")