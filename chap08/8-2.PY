# 빈 자리는 빈 공간으로 두고, 오른쪽으로 정렬하기
# 다만 총 10자리 공간을 확보해야 함
print("{0: >10}".format(500))

# 양수는 앞에 + 붙이고 음수는 앞에 - 붙임
print("{0: >+10}".format(500))
print("{0: >+10}".format(-500))

# 왼쪽 정렬, 빈 칸을 _로 채움
print("{0:_<+10}".format(500))

# 3자리마다 ,를 찍어짐
print("{0:,}".format(1000000000))

# 3자리마다 , 찍고 +- 부호까지
print("{0:+,}".format(1000000000))
print("{0:+,}".format(-1000000000))

# 3자리마다 , 찍고 부호 붙이고 자리수 확보
# 빈 칸은 _로 채워줌
print("{0:_<+30,}".format(1000000000))

# 소수점 출력
print("{0:f}".format(5/3))

# 소수점 둘째자리까지 출력
print("{0:.2f}".format(5/3))